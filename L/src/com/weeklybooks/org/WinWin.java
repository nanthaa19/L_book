package com.weeklybooks.org;

public class WinWin implements WeeklyBooks
{
	private final String bookName , launchDay;
	
	public WinWin()
	{
		bookName = "Win Win";
		launchDay = "Monday";
	}
	
	@Override
	public String getBookName()
	{		
		return bookName;
	}

	@Override
	public String getLaunchDay()
	{	
		return launchDay;
	}
}

